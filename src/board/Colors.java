package board;

/**
 *
 * 
 * La clase Colors representa los colores usables en el programa. Vease de fondo
 * o de texto
 * 
 * 
 *
 * @author Jose Miguel Vazquez Moreno
 * @version 1.0
 * @since 1.0
 *
 */
public class Colors {

	// RESET
	public static final String RESET = "\u001B[0m";

	// BACKGROUNDS
	public static final String WHITE_BG = "\u001B[47m";
	public static final String BLACK_BG = "\u001B[40m";
	public static final String RED_BG = "\u001B[41m";
	public static final String LIGHT_YELLOW_BG = "\u001B[103m";
	public static final String LIGHT_GREEN_BG = "\u001B[102m";

	// TEXTS
	public static final String BLACK_TEXT = "\u001B[30m";
	public static final String RED_TEXT = "\u001B[31m";
	public static final String GREEN_TEXT = "\u001B[32m";
	public static final String YELLOW_TEXT = "\u001B[33m";
	public static final String BLUE_TEXT = "\u001B[34m";
	public static final String CYAN_TEXT = "\u001B[36m";
	public static final String WHITE_TEXT = "\u001B[37m";
	public static final String LIGHT_RED_TEXT = "\u001B[91m";
}
